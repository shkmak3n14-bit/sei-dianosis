		storedWingState &&
		storedWingState.type === topResult.type &&
		Array.isArray(storedWingState.wings) &&
		storedWingState.wings.length === wingCodes.length &&
		storedWingState.wings.every((wingCode, index) => wingCode === wingCodes[index])
