function getPreviewTypeFromQuery() {
	const params = new URLSearchParams(window.location.search);
	const previewType = Number(params.get("previewType"));

	if (Number.isInteger(previewType) && previewType >= 1 && previewType <= 9) {
		return previewType;
	}

	return null;
}

